def ensure_personality_file():
    if not os.path.exists(PERSONALITY_PATH):
        with open(PERSONALITY_PATH, "w", encoding="utf-8") as file:
            file.write(DEFAULT_PERSONALITY + "\n")


def load_personality():
    ensure_personality_file()

    with open(PERSONALITY_PATH, "r", encoding="utf-8") as file:
        text = file.read().strip()

    return text or DEFAULT_PERSONALITY
